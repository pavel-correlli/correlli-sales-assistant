def get_lang() -> str:
    lang = st.session_state.get("ui_lang_v1", "en")
    if lang not in LANGUAGES:
        lang = "en"
    st.session_state["ui_lang_v1"] = lang
    return lang


def set_lang(lang: str) -> None:
    if lang in LANGUAGES:
        st.session_state["ui_lang_v1"] = lang


def t(key: str, **kwargs) -> str:
    lang = get_lang()
    base = TRANSLATIONS.get("en", {})
    txt = TRANSLATIONS.get(lang, {}).get(key, base.get(key, key))
    if kwargs:
        try:
            return txt.format(**kwargs)
        except Exception:
            return txt
    return txt


def market_name(code: str) -> str:
    lang = get_lang()
    key = str(code or "").strip().upper()
    return MARKET_NAMES.get(key, {}).get(lang, key if key else "-")


def market_label(code: str) -> str:
    key = str(code or "").strip().upper()
    if not key:
        return "-"
    return f"{key} ({market_name(key)})"


def pipeline_label(name: str) -> str:
    raw = str(name or "").strip()
    if not raw:
        return "-"
    if get_lang() != "ru":
        return raw

    if "|" in raw:
        left, right = [p.strip() for p in raw.split("|", 1)]
        left_code = left.upper()
        left_display = market_name(left_code) if left_code in MARKET_NAMES else left
        return f"{left_display} | {right}"

    key = raw.upper()
    if key in MARKET_NAMES:
        return market_name(key)

    return raw


def call_type_label(value: str) -> str:
    raw = str(value or "").strip()
    if not raw:
        return "-"
    lang = get_lang()
    return CALL_TYPE_LABELS.get(raw, {}).get(lang, raw)
